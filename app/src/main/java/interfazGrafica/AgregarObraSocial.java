    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            if (nombre.getText().isEmpty() || codigo.getText().isEmpty()) {

                throw new CamposInCompletosException("Por Favor Complete Todos Los Campos");
            }

            String txtNombre = this.nombre.getText();

            Integer txtCodigo = Integer.parseInt(this.codigo.getText());

            ObraSocial obrasocial = new ObraSocial(txtNombre, txtCodigo);

           
            consultorio.agregarObraSocial(obrasocial);

            JOptionPane.showMessageDialog(this, "Obra Social  Agregada con Exito");

        } catch (ObraSocialExisteException e) {

            JOptionPane.showMessageDialog(this, "La Obra Social Ya Se Encuentra Registrada ");
        } catch (CamposInCompletosException e) {
            JOptionPane.showMessageDialog(this, "Debe Completar Todos Los Campos");
        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(this, "El codigo solo debe contener numeros");
        }


        nombre.setText(null);
        codigo.setText(null);


    }//GEN-LAST:event_btnAgregarActionPerformed

