    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {

            if (nombre.getText().isEmpty()) {

                throw new CamposInCompletosException("Por Favor Complete Todos Los Campos");
            }

            obraSocial.setNombreDeObraSocial(this.nombre.getText());

            JOptionPane.showMessageDialog(null, "Datos De Obra Social Modificados Con Exito");
            this.dispose();

        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(this, "El codigo solo debe contener numeros");

        } catch (CamposInCompletosException ex) {

            JOptionPane.showMessageDialog(this, "Por Favor Complete Todos Los Campos");

        }


    }//GEN-LAST:event_btnModificarActionPerformed

